// Writes the given image to top left corner of the framebuffer, where each
// pixel is a pixel_size x pixel_size box
void write_image_to_framebuffer(struct IMAGE image , int pixel_size)
{
    clear_framebuffer();

    int img_height = image.height;
    int img_width = image.width;
    const uint32_t* pixel_data = image.pixel_data;


    for (int curr_y = img_height-1; curr_y >= 0; curr_y--) {
        for (int curr_x = 0; curr_x < img_width; curr_x++) {
            int hex = *pixel_data++;

            int red = hex >> 16;
            int green = (hex & 0x00ff00) >> 8;
            int blue = hex & 0x0000ff;
            int color_index = get_nearest_vga256_color(
                (struct RGB_DATA) {red, green, blue}
            );

            draw_box(curr_x * pixel_size,
                     curr_y * pixel_size,
                     pixel_size,
                     pixel_size,
                     color_index);
        }
    }
}

// Entry point for image displaying
// Selects an IMAGE struct by the given enum and passes it along
void display_image(enum IMAGE_NAME image_name, int pixel_size)
{
    const struct IMAGE* to_display;
    switch (image_name) {
        case BARS:
            to_display = &IMG_BARS;
            break;
        case SWEET_TOOTH:
            to_display = &IMG_SWEET_TOOTH;
            break;
        case CYMK:
            to_display = &IMG_CYMK;
            break;
        case KARMA:
            to_display = &IMG_KARMA;
            break;
        case LAMBDA:
            to_display = &IMG_LAMBDA;
            break;
    }
    write_image_to_framebuffer(*to_display, pixel_size);
}

void print_image_info()
{
    for (int i = 0; i < IMAGE_COUNT; i++) {
        print_char('>');
        print_line((char*) ALL_IMAGES[i].name_str);
        print_string("\t");
        print_line((char*) ALL_IMAGES[i].description);
    }
}

enum IMAGE_NAME str2img_name(char *str)
{
    for (int i = 0; i < IMAGE_COUNT; i++) {
        if (streq(str, ALL_IMAGES[i].name_str)) {
            return ALL_IMAGES[i].name_enum;
        }
    }
    return -1;
}

