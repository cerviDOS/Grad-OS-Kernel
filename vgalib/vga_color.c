// Returns val^2
int square (int val)
{
    return val*val;
}

// Calculates and returns the squared distance formula using the
// specified colors
int distance_squared(struct RGB_DATA a, struct RGB_DATA b)
{
    return (square(b.red - a.red))
    + (square(b.green - a.green))
    + (square(b.blue - a.blue));
}

// Searches the above color array for color with the smallest
// Euclidean distance
//
// Adapted from a personal project of mine involving color quantization
int get_nearest_vga256_color(struct RGB_DATA color)
{
    int nearest_index = 0;
    int shortest_dist = INT32_MAX;
    int curr_dist = 0;

    for (int palette_index = 0; palette_index < 256; palette_index++) {

        curr_dist = distance_squared(color, VGA_PALETTE_256[palette_index]);
        if (curr_dist < shortest_dist) {
            nearest_index = palette_index;
            shortest_dist = curr_dist;
        }
    }
    return nearest_index;
}


